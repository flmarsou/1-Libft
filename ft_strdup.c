#include "libft.h"

char	*ft_strdup(const char *str)
{
	char			*dup;
	unsigned int	i;

	dup = malloc(sizeof(char) * ft_strlen(str) + 1);
	if (dup == NULL)
	{
		return (NULL);
	}
	i = 0;
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
